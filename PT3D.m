clear;
clc;

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%% constant %%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
a = 0;
c = 1;
d = 1;
f = 1;
e = 1;
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%% energy  %%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
Ei = [];
Er = [];
X = [];
for b = -3:0.02:3;
A = a+b*i;
B = a-b*i;
H = [A d e; f c f; e d B];%创建矩阵
E = eig(H);
Ei = [Ei; imag(E(1));imag(E(2));imag(E(3))];%记录虚部
Er = [Er; real(E(1));real(E(2));real(E(3))];%记录实部
X = [X; b;b;b];%建立x轴
end
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%% plot %%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
axis([-3 3 -3 3]);
plot(X,Ei,'bo' ,'MarkerSize',3);
hold on;
plot(X,Er,'r.' ,'MarkerSize',9);
legend('Im(E)','Re(E)');
xlabel('g=b/c');
ylabel('E');